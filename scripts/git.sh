#!/bin/sh

fn_git_msg_commit() {
	echo "feat: for a new feature or enhancement."
	echo "fix: for a bug fix."
	echo "docs: for changes to documentation."
	echo "style: for code formatting changes (no functionality change)."
	echo "refactor: for code changes that do not add new functionality or fix bugs."
	echo "test: for adding or modifying tests."
	echo "chore: for changes to maintenance or configuration tasks that do not add new functionality or fix bugs."
}
